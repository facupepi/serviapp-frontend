// Función para obtener todas las provincias
export const getProvinces = (): string[] => {
  return ARGENTINA_DATA.map(item => item.province);
};

// Función para obtener localidades de una provincia
export const getLocalitiesByProvince = (province: string): string[] => {
  const provinceData = ARGENTINA_DATA.find(item => item.province === province);
  return provinceData ? provinceData.localities : [];
};

// Función para crear objeto de localidades por provincia
export const getLocalitiesObject = (): Record<string, string[]> => {
  const result: Record<string, string[]> = {};
  ARGENTINA_DATA.forEach(item => {
    result[item.province] = item.localities;
  });
  return result;
};
